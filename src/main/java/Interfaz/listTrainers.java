    public void mostrar(String tabla) throws SQLException{
        String sql = "SELECT * FROM " + tabla;
        Statement st;
        
        ConnectDB con = new ConnectDB();
        Connection link = con.Connect();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("rut_trainer");
        model.addColumn("first_name");
        model.addColumn("last_name");
        model.addColumn("specialty");
        model.addColumn("birth_date");
        table.setModel(model);
        
        String [] datos = new String[5];
        
        try{
            st = link.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                model.addRow(datos);
                        
            }
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error");
        }
    }
