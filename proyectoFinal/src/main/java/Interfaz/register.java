    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        
        ConnectDB conn = new ConnectDB();
        Connection link = conn.Connect();
        PatientG patient = new PatientG();
        Patient pat = new Patient();
        Login log = new Login();
        
        if(name.getText().equals("") || date.getText().equals("") || email.getText().equals("")|| lastName.getText().equals("") || pass.getText().equals("") || rut.equals("")){
        JOptionPane.showMessageDialog(null, "Hay celdas vacias");
        }
        else{
            if(patient.Validate(link, parseInt(rut.getText()))==0) {
                
                pat.setFirst_name(name.getText());
                pat.setLast_name(lastName.getText());
                pat.setRut(parseInt(rut.getText()));
                pat.setEmail(email.getText());
                pat.setPassword(pass.getText());
                try {
                    Date birth_date = new SimpleDateFormat("dd/mm/yyyy").parse(date.getText());
                    pat.setBirth_date(new java.sql.Date(birth_date.getTime()));
                } catch (ParseException e) {
                    throw new RuntimeException(e);
                }
                if(patient.Create(conn.Connect(), pat)== true){
                    JOptionPane.showMessageDialog(null, "Se ha registrado exitosamente");
                    log.setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Se produjo un error");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Se produjo un error");
            }
        }
    }//GEN-LAST:event_registerActionPerformed

